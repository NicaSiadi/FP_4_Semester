void FSlickNodesModule::SetStyle(const TSoftObjectPtr<UTexture2D> CustomAsset, FSlateStyleSet* Style, const FName& Type,
    const FString& TexturePath, bool bIsBoxBrush, const FVector2D& Size, const FMargin& Margin)
{
    const USlickNodesConfig* Settings = GetDefault<USlickNodesConfig>(); 
    
    if (CustomAsset.LoadSynchronous() && Settings->bOverwriteNodes)
    {
        UTexture2D* LoadedAsset = CustomAsset.LoadSynchronous();
        if (bIsBoxBrush)
        {
            FSlateBoxBrush* CustomBoxBrush = new FSlateBoxBrush(LoadedAsset, Margin);
            Style->Set(Type, CustomBoxBrush);
        }
        else
        {
            FSlateImageBrush* CustomImageBrush = new FSlateImageBrush(LoadedAsset, Size);
            Style->Set(Type, CustomImageBrush);
        }
    }
    else
    {
        if (bIsBoxBrush)
        {
            FSlateBoxBrush* BoxBrush = new BOX_BRUSH(TexturePath, Margin);
            Style->Set(Type, BoxBrush);
        }
        else
        {
            FSlateImageBrush* ImageBrush = new IMAGE_BRUSH(TexturePath, Size);
            Style->Set(Type, ImageBrush);
        }
    }
    
}

void FSlickNodesModule::SetBaseColors()
{
    UGraphEditorSettings* GraphSettings = GetMutableDefault<UGraphEditorSettings>();
    const USlickNodesConfig* Settings = GetDefault<USlickNodesConfig>(); 
    if (GraphSettings && Settings)
    {
        if (Settings->bOverwriteColors)
        {
            
            GraphSettings->EventNodeTitleColor = Settings->EventNodeColor;
            GraphSettings->FunctionCallNodeTitleColor = Settings->FunctionCallNodeColor;
            GraphSettings->PureFunctionCallNodeTitleColor = Settings->PureFunctionNodeColor;
            GraphSettings->ParentFunctionCallNodeTitleColor = Settings->ParentFunctionNodeColor;
            GraphSettings->FunctionTerminatorNodeTitleColor = Settings->FunctionTerminatorColor;
            GraphSettings->ExecBranchNodeTitleColor = Settings->BranchNodeColor;
            GraphSettings->ExecSequenceNodeTitleColor = Settings->SequenceNodeColor;
            GraphSettings->ResultNodeTitleColor = Settings->ResultNodeColor;
            GraphSettings->DefaultCommentNodeTitleColor = Settings->DefaultCommentNodeColor;
            GraphSettings->PreviewNodeTitleColor = Settings->PreviewNodeColor;
                    
            //GraphSettings->SaveConfig();
            
        }
        
    }
}

